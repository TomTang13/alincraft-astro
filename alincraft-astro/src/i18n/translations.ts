export function getLangContent(locale: string, section: string) {
  const enContent = translations['en'].sections[section];
  if (translations[locale]?.sections?.[section]) {
    const localContent = translations[locale].sections[section];
    // 合并所有字段
    return {
      ...enContent,
      ...localContent,
      title: localContent.title || enContent.title,
      content: localContent.content || enContent.content,
      cta: localContent.cta || enContent.cta || '',
      address: localContent.address || enContent.address || '',
      wechat: localContent.wechat || enContent.wechat || '',
      etsy: localContent.etsy || enContent.etsy || ''
    };
  }
  return enContent;
}

export function getText(locale: string, key: string) {
  if (translations[locale]?.[key]) {
    return translations[locale][key];
  }
  if (translations['en'][key]) {
    return translations['en'][key];
  }
  // Default values for missing translations
  const defaults: Record<string, string> = {
    products: locale.startsWith('zh') ? '商品展示' : 'Products',
    productsTagline: locale.startsWith('zh') ? '手工定制，匠心独运' : 'Handmade with Love',
    viewDetails: locale.startsWith('zh') ? '查看详情' : 'View Details',
    backToProducts: locale.startsWith('zh') ? '返回商品' : 'Back to Products',
    size: locale.startsWith('zh') ? '尺寸' : 'Size',
    color: locale.startsWith('zh') ? '颜色' : 'Color',
    digitalNote: locale.startsWith('zh') ? '购买后您将收到PDF下载链接' : 'You will receive a download link after purchase',
    customizeNow: locale.startsWith('zh') ? '联系定制' : 'Customize Now'
  };
  return defaults[key] || key;
}
